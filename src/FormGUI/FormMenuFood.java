                                + "','" + textJumlahOri_Kelompok03.getText()+ "','" + textTotalOri_Kelompok03.getText()+ "','" + textKdBrgOri_Kelompok03.getText()+"')";

                        java.sql.Connection conn=(Connection)Database.configDB();
