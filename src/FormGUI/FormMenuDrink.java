                  + "','" + textJumlahOri_Kelompok4.getText()+ "','" + textTotalOri_Kelompok4.getText()+ "','" + textKdBrgOri_Kelompok4.getText()+"')";

                        java.sql.Connection conn=(Connection)Database.configDB();
