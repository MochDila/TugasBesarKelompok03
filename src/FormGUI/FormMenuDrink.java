    private void labelPesanOriginal_Kelompok6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_labelPesanOriginal_Kelompok6MouseClicked
        // TODO add your handling code here:
        try {
        if(textEmailOri_Kelompok6.getText().equals("") || textJumlahOri_Kelompok6.getText().equals("") 
                   || textTotalOri_Kelompok6.getText().equals("")){
                JOptionPane.showMessageDialog(rootPane, "Data Tidak Boleh Kosong", "Pesan", JOptionPane.ERROR_MESSAGE);
                textEmailOri_Kelompok6.requestFocus();
            }
        else if (textJumlahOri_Kelompok6.getText().equals("0")){
            JOptionPane.showMessageDialog(rootPane, "Jumlah Pemesanan Tidak Boleh 0", "Pesan", JOptionPane.ERROR_MESSAGE);
        }
            else{
                st = con.createStatement();
                String sql = ("SELECT * FROM t_akun_profile_pelanggan WHERE email ='" + textEmailOri_Kelompok6.getText()+"'");
                ResultSet rs = st.executeQuery(sql); 
                if(rs.next()){
                    try{
                        String sql2 = "INSERT INTO  t_keranjang VALUES ('"+textKodeKeranjang_Kelompok03.getText()+ "','" + textNamaBrgOri_Kelompok6.getSelectedItem()
                  + "','" + textHargaOril_Kelompok6.getText()+ "','" + textEmailOri_Kelompok6.getText()+ "','" + tgl_kelompok03.getText()
                  + "','" + textJumlahOri_Kelompok6.getText()+ "','" + textTotalOri_Kelompok6.getText()+ "','" + textKdBrgOri_Kelompok6.getText()+"')";

                        java.sql.Connection conn=(Connection)Database.configDB();
                        java.sql.PreparedStatement pstm = conn.prepareStatement(sql2);
                        pstm.execute();  
                        JOptionPane.showMessageDialog(null, "Pemesanan Berhasil");
                        kodeKeranjangOtomatis();
                    }catch(HeadlessException | SQLException e){
                        JOptionPane.showMessageDialog(this, "Simpan Data Gagal", "Pesan",
                        JOptionPane.WARNING_MESSAGE);
                    }
                }
                else{
                    JOptionPane.showMessageDialog(rootPane, "Email Belum Terdaftar", "Pesan",
                    JOptionPane.ERROR_MESSAGE);
                }
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_labelPesanOriginal_Kelompok6MouseClicked

